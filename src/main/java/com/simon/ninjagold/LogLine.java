package com.simon.ninjagold;

public class LogLine {
	private String logLine;
	private String color;
	
	public LogLine(String logLine, String color) {
		this.logLine = logLine;
		this.color = color;
	}

	public String getLogLine() {
		return logLine;
	}

	public String getColor() {
		return color;
	}
}
